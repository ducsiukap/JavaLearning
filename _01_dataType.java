public class _01_dataType {
    public static void main(String[] args) {
        // syntax: dataType varName [= value];
        /*
         * Primitive data type:
         * - boolean: true/false
         * - char: 0 -> 255
         * - byte: -128 -> 127
         * - short: 2bytes
         * - int: 4bytes
         * - long: 8bytes
         * - float: 4bytes
         * - double: 8bytes
         */
        int iNumber = 23;
        float fNumber = 2.345f;
        System.out.println("iNumber:integer = " + iNumber);
        System.out.printf("fNumber:float = %.2f", fNumber);

        /*
         * Non_primitive data type:
         * String
         * Class
         * Object
         * Interface
         * Array
         */
    }
}
